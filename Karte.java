
public class Karte

{
	public String nr;
	public String farbe;
	
	Karte(){}
	
	Karte(String nr, String farbe)
	{
		nr = this.nr;
		farbe = this.nr;
	}

	public String getNr() {
		return nr;
	}

	public void setNr(String nr) {
		this.nr = nr;
	}

	public String getFarbe() {
		return farbe;
	}

	public void setFarbe(String farbe) {
		this.farbe = farbe;
	}
	
	
	

}
